C-   TPK ALGORITH IN FORTRAN 66
     FTPK(X) = SQRT(ABS(X)) + 5.0*X**3
C-   MAIN PROGRAM
     DIMENSION A(11)
     N=11
     PRINT 100
100  FORMAT("PLEASE ENTER 11 NUMBERS")
     READ 101,A
101  FORMAT(F9.4)
     PRINT 102
102  FORMAT("RESULTS ARE")
     DO 3 J = 1, N
     K = N - J + 1
     RESULT = FTPK(A(K))
     IF (RESULT .LE. 400) GOTO 2
1    PRINT 103
103  FORMAT("TOO LARGE")
     GOTO 3
2    PRINT 101,RESULT
3    CONTINUE
     STOP
     END
