C     TPK ALGORITH IN FORTRAN II
      FTPKF(X)=SQRTF(ABSF(X))+5.0*X**3
C     MAIN PROGRAM
      DIMENSION A(11)
      N=11
      PRINT 100
 100  FORMAT(23HPLEASE ENTER 11 NUMBERS)
      READ 101,A
 101  FORMAT(F9.4)
      PRINT 102
 102  FORMAT(11HRESULTS ARE)
      DO 3 J=1,N
      RESULT=FTPKF(A(J))
      IF (RESULT-400.0) 2,2,1
 1    PRINT 103
 103  FORMAT(9HTOO LARGE)
      GO TO 3
 2    PRINT 101,RESULT
 3    CONTINUE
      STOP
      END
